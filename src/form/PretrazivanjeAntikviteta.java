    private void populateForm() {
        try {
            tblAntikviteti.setModel(new AntikvitetiTableModel());
            n = new NitTabele(tblAntikviteti, ModelTabele.ANTIKVITET_MODEL);
        } catch (Exception ex) {
            Logger.getLogger(PretrazivanjeAntikviteta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void uzmiAntikvitete() {
        try {
            antikviteti = Komunikacija.getInstance().UcitajAntikvitete();
        } catch (Exception ex) {
            Logger.getLogger(PretrazivanjeAntikviteta.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void pretragaAntikviteta() {
        uzmiAntikvitete();
        pretraga = new ArrayList<>();
        for (Antikvitet antikvitet : antikviteti) {
            if (antikvitet.getNaziv().toLowerCase().contains(strPretraga)) {
                pretraga.add(antikvitet);
            }
        }

    }
