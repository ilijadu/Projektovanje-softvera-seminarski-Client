    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        if (tblAntikviteti.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da prikaže podatke antikviteta. Molomo Vas da izaberete antikvitet pre prikazivanja detalja! ", "Poruka", JOptionPane.INFORMATION_MESSAGE);
        } else if (pretraga != null) {
            Antikvitet a = pretraga.get(tblAntikviteti.getSelectedRow());
            System.out.println("forms.PretrazivanjeAntikviteta.btnDetaljiActionPerformed(), antikvitet za prikaz: " + a);
            new KreiranjeAntikviteta(FormView.FORM_VIEW_MODE, a, this,null);

        } else {
            uzmiAntikvitete();
            Antikvitet a = antikviteti.get(tblAntikviteti.getSelectedRow());
            System.out.println("forms.PretrazivanjeAntikviteta.btnDetaljiActionPerformed(), antikvitet za prikaz: " + a);
            new KreiranjeAntikviteta(FormView.FORM_VIEW_MODE, a, this,null);
            tblAntikviteti.getSelectionModel().clearSelection();

        }
    }//GEN-LAST:event_btnDetaljiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        strPretraga = txtPretraga.getText().toLowerCase();
        if (!strPretraga.isEmpty()) {
            pretragaAntikviteta();
            if (pretraga.size() == 0) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da nađe antikvitet po zadatoj vrednosti.Ne postoji antikvitet sa tim imenom!");
            } else if (pretraga.size() > 1) {
                tblAntikviteti.setModel(new AntikvitetiTableModel(pretraga));
                JOptionPane.showMessageDialog(this, "Sistem je pronašao više rezultata. Molimo Vas da odaberete neki od prikazanih antikviteta.");
            } else {
                tblAntikviteti.setModel(new AntikvitetiTableModel(pretraga));
                JOptionPane.showMessageDialog(this, "Sistem je našao antikvitet po zadatoj vrednosti.");
            }
            tblAntikviteti.getSelectionModel().clearSelection();
        } else {
            pretraga = null;
            JOptionPane.showMessageDialog(this, "Pre pretrage unesite karaktere za pretragu.");
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

