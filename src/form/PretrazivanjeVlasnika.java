    private void populateForm() {
        try {
            tblVlasnici.setModel(new VlasniciTableModel());
            n = new NitTabele(tblVlasnici, ModelTabele.VLASNIK_MODEL);
        } catch (Exception ex) {
            Logger.getLogger(PretrazivanjeVlasnika.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void pretragaVlasnika() {
        uzmiVlasnike();
        pretraga = new ArrayList<>();
        for (Vlasnik v : vlasnici) {
            String imePr = v.getIme().toLowerCase() + " " + v.getPrezime().toLowerCase();

            if (v.getIme().toLowerCase().contains(strPretraga) || v.getPrezime().toLowerCase().contains(strPretraga) || imePr.contains(strPretraga)) {
                pretraga.add(v);
            }
        }

    }

    private void uzmiVlasnike() {
        try {
            vlasnici = Komunikacija.getInstance().UcitajVlasnike();
        } catch (Exception ex) {
            Logger.getLogger(PretrazivanjeVlasnika.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
