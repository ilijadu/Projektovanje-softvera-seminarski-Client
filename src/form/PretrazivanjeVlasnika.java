    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        if (tblVlasnici.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da prikaže podatke vlasnika. Molomo Vas da izaberete vlasnika pre prikazivanja detalja! ", "Poruka", JOptionPane.OK_OPTION);
        } else if (pretraga != null) {
            //n.promeniSignal();
            Vlasnik v = pretraga.get(tblVlasnici.getSelectedRow());
            new KreiranjeVlasnika(FormView.FORM_VIEW_MODE, v, this);

        } else {
            //n.promeniSignal();
            uzmiVlasnike();
            Vlasnik v = vlasnici.get(tblVlasnici.getSelectedRow());
            new KreiranjeVlasnika(FormView.FORM_VIEW_MODE, v, this);

        }
        tblVlasnici.getSelectionModel().clearSelection();
    }//GEN-LAST:event_btnDetaljiActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        strPretraga = txtPretraga.getText().toLowerCase();
        if (!strPretraga.isEmpty()) {
            pretragaVlasnika();
            if (pretraga.size() == 0) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da nađe vlasnika po zadatoj vrednosti.Ne postoji vlasnik sa tim imenom ili prezimenom!");
            } else if (pretraga.size() > 1) {
                System.out.println("forms.PretrazivanjeVlasnika.pretragaVlasnika(), prtraga: " + pretraga);
                tblVlasnici.setModel(new VlasniciTableModel(pretraga));
                JOptionPane.showMessageDialog(this, "Sistem je pronašao više rezultata. Molimo Vas da odaberete nekog od prikazanih vlasnika.");
            } else {
                tblVlasnici.setModel(new VlasniciTableModel(pretraga));
                JOptionPane.showMessageDialog(this, "Sistem je našao vlasnika po zadatoj vrednosti.");

            }
            tblVlasnici.getSelectionModel().clearSelection();
        } else {
            pretraga = null;
            JOptionPane.showMessageDialog(this, "Pre pretrage unesite karaktere za pretragu.");
        }
    }//GEN-LAST:event_btnPretraziActionPerformed

